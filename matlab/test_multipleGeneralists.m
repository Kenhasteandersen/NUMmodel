

%--------------------------------------------------------------------------
% Define number of generalists
%--------------------------------------------------------------------------
n1=10;
n2=10;

%--------------------------------------------------------------------------
% Initiate and call library
%--------------------------------------------------------------------------
loadNUMmodelLibrary(false);
calllib(loadNUMmodelLibrary(), 'f_setupgeneralistssimple_two', int32(n1), int32(n2));
%--------------------------------------------------------------------------
% Setup nutrients (N) and DOC
%--------------------------------------------------------------------------
p = setupNutrients_N_DOC;
%--------------------------------------------------------------------------
% Setup the parameters for the two types of generalists
%--------------------------------------------------------------------------
% Generalists1:
p = parametersAddgroup(1,p,n1);

% Generalists2:
p = parametersAddgroup(1,p,n2);
%--------------------------------------------------------------------------
% calculate the mass of the organisms
%--------------------------------------------------------------------------
p = getMass(p);

%--------------------------------------------------------------------------
% Initial conditions
%--------------------------------------------------------------------------
p.u0(1:2) = [150, 0]; % nutrients
p.u0(p.idxB:p.n) = 1; % biology

p.tEnd = 200;
p.d = 0.1;
